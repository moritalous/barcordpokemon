package morita.kazuaki.barcordpokemon.model;

public class PokemonModel {

	private String largeImagePath;
	private String nameImagePath;
	private String pokemonName;

	public String getLargeImagePath() {
		return largeImagePath;
	}

	public void setLargeImagePath(String largeImagePath) {
		this.largeImagePath = largeImagePath;
	}

	public String getNameImagePath() {
		return nameImagePath;
	}

	public void setNameImagePath(String nameImagePath) {
		this.nameImagePath = nameImagePath;
	}

	public String getPokemonName() {
		return pokemonName;
	}

	public void setPokemonName(String pokemonName) {
		this.pokemonName = pokemonName;
	}

}
